def main():
    # Dedup by name, keeping the first (non-compatibility) categorization where a name
    # appears twice (e.g. FLOOR / CONCATENATE listed under both their modern category
    # and compatibility). A name is counted ONCE in the canonical list.
    seen = {}
    for name, category, importance in FUNCTIONS:
        if name not in seen:
            seen[name] = (name, category, importance)
    rows = sorted(seen.values())

    out_path = os.path.join(
        os.path.dirname(os.path.dirname(os.path.abspath(__file__))),
        "data",
        "excel_functions_canonical.csv",
    )
    with open(out_path, "w", newline="", encoding="utf-8") as fh:
        fh.write(
            "# Canonical Excel function list -- Microsoft 'Excel functions "
            "(alphabetical)'/'(by category)' catalog for Excel for Microsoft 365.\n"
        )
        fh.write(
            "# Provenance: constructed manually 2026-07-01 (live fetch of "
            "support.microsoft.com was blocked by the container egress policy; "
            "functional_spec SP3 permits this documented-construction fallback).\n"
        )
        fh.write(
            "# importance: common=everyday/ubiquitous (the off-ramp set); "
            "obscure=specialist. See scripts/build_canonical_excel_list.py for the "
            "full rubric + source URLs.\n"
        )
        writer = csv.writer(fh)
        writer.writerow(["name", "category", "importance"])
        for name, category, importance in rows:
            writer.writerow([name, category, importance])

    common = sum(1 for _, _, imp in rows if imp == "common")
    print(f"wrote:   {out_path}")
    print(f"count:   {len(rows)} distinct canonical Excel functions")
    print(f"common:  {common}   obscure: {len(rows) - common}")


if __name__ == "__main__":
    main()
